     */
    public function index(Request $request)
    {

        $isAdmin = $request->user()->hasTeamPermission($request->user()->currentTeam, 'admin');

        if ($isAdmin) {
            return Inertia::render('Auth/Register');
        }

        return Inertia::render('Auth/WantRegister');
    }

